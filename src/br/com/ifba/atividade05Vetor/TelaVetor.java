    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
          // Verifica se ainda existe espaço no vetor
        if (selecionado < vetor.length) {

            // Pega o valor escolhido pelo usuário no JSpinner
            int numero = (int) jspNumero.getValue();

            // Guarda o número no vetor na posição atual
            vetor[selecionado] = numero;

            // Avança para a próxima posição livre
            selecionado++;

            // Atualiza a lista na tela
            preencherLista();

        } else {

            // Mostra mensagem caso o vetor esteja cheio
            javax.swing.JOptionPane.showMessageDialog(this, "O vetor já está cheio.");
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // Verifica se existe algum elemento para remover
        if (selecionado > 0) {

            // Volta uma posição
            selecionado--;

            // Zera o último valor inserido
            vetor[selecionado] = 0;

            // Atualiza a lista na tela
            preencherLista();

        } else {

            // Mostra mensagem caso não exista elemento para remover
            javax.swing.JOptionPane.showMessageDialog(this, "Não há elementos para remover.");
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnOrdenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrdenarActionPerformed
        // Ordena o vetor em ordem crescente
        Arrays.sort(vetor);

        // Atualiza a lista na tela após ordenar
        preencherLista();
    }//GEN-LAST:event_btnOrdenarActionPerformed

