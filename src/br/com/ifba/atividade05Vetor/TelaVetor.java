    // Método responsável por atualizar os dados mostrados na JList
    public void preencherLista() {

         // Limpa a lista
        lista.removeAllElements();

         // Percorre o vetor
        for (int i = 0; i < vetor.length; i++) {

        // Adiciona os elementos na lista
        lista.addElement("[" + i + "] -> " + vetor[i]);
    }
    }


